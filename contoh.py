#tipe data boolean
print(True)

#tipe data string 
print("string dengan menggunakan tanda kutip dua")
print('ini string menggunakan tanda kutip satu')
#tipe data string dengan menjelaskan special karakter atau escape sequences
print('ini adalah tanda single quote (\')')
print("ini adalah tanda double quote (\")")
print("ini adalah tanda slash (\\)")
print("Algoritma\nPemograman") #menggunakan \n untuk membuat baris baru
print("Algoritma\bPemograman") #menggunakan \b untuk menghilangkan karakter/satu hurup sebelum tanda \b
print("Algoritma\tPemograman") #menggunakan \t untuk membuat tab
print("Algoritma\rPemograman") #menggunakan \r untuk 

#tipe data integer
print(20)

#tipe data float
print(3.14)
print(.2)
print(4.2e-3)

#tipe data binary
print(0b10)

#tipe data octal
print(0o10)

#tipe data hexadecimal
print('tipe data hexadecimal',0x10)

#tipe data complex
print(5j)

#tipe data list
print([1,2,3,4,5])
print(["satu","dua","tiga"])

#tipe data tuple
print((1,2,3,4,5))
print(("satu","dua","tiga"))

#tipe data dictionary
print({"Nama":"Risda", 'umur':19})
#tipe data dictionary dimasukan ke dalam variable biodata
biodata = {"Nama":"Risda", 'umur':19} #proses inisialisasi variable biodata
print(biodata) #proses pencetakan variable biodata yang berisi tipe data dictionary
type(biodata) #fungsi untuk mengecek jenis tipe data yang akan tampil <class 'dict'> yang berarti tipe data dictionary